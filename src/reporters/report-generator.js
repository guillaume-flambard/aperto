const fs = require('fs-extra');
const chalk = require('chalk');

async function generateReport(analysis, format) {
  switch (format) {
    case 'markdown':
      await generateMarkdownReport(analysis);
      break;
    case 'json':
      await generateJsonReport(analysis);
      break;
    default:
      generateConsoleReport(analysis);
  }
}

function generateConsoleReport(analysis) {
  console.log(chalk.blue('\n📊 Analysis Report\n'));
  
  console.log(chalk.white('Stack:'), chalk.yellow(analysis.stack.name));
  console.log(chalk.white('Files:'), chalk.yellow(analysis.files));
  console.log(chalk.white('Routes:'), chalk.yellow(analysis.summary.totalRoutes));
  console.log(chalk.white('Tests:'), chalk.yellow(analysis.summary.totalTests));
  console.log(chalk.white('Coverage:'), chalk.yellow(`${analysis.summary.coverage}%`));
  
  if (analysis.scopes?.length > 0) {
    console.log(chalk.blue('\n📦 Scopes:\n'));
    analysis.scopes.forEach(scope => {
      const status = scope.completion >= 80 ? chalk.green('✓') :
                    scope.completion >= 50 ? chalk.yellow('○') : chalk.red('✗');
      console.log(`  ${status} ${scope.name}: ${scope.completion}%`);
    });
  }
  
  console.log('');
}

async function generateMarkdownReport(analysis) {
  const content = `# Aperto Analysis Report

**Date:** ${analysis.timestamp}
**Project:** ${process.cwd()}

## Stack Information

- **Framework:** ${analysis.stack.name}
- **Version:** ${analysis.stack.version || 'N/A'}

## Summary

- **Total Files:** ${analysis.files}
- **Total Routes:** ${analysis.summary.totalRoutes}
- **Total Tests:** ${analysis.summary.totalTests}
- **Coverage:** ${analysis.summary.coverage}%
- **Critical Scopes:** ${analysis.summary.criticalScopes}
- **Incomplete Scopes:** ${analysis.summary.incompleteScopes}

## Scopes

${analysis.scopes?.map(s => `
### ${s.name}

- **Type:** ${s.type}
- **Routes:** ${s.routes || s.count || 0}
- **Completion:** ${s.completion}%
- **Priority:** ${s.priority}/100
`).join('\n') || 'No scopes detected'}

## Recommendations

${analysis.summary.coverage < 30 ? 
  '- **Priority:** Start with scope-first mode\n- **Target:** Critical scope with missing tests' :
  '- **Status:** Good coverage, consider audit mode'
}

---

*Generated by Aperto*
`;

  await fs.writeFile('APERTO_REPORT.md', content);
  console.log(chalk.green('\n✅ Report saved: APERTO_REPORT.md'));
}

async function generateJsonReport(analysis) {
  await fs.writeJson('aperto-report.json', analysis, { spaces: 2 });
  console.log(chalk.green('\n✅ Report saved: aperto-report.json'));
}

module.exports = { generateReport };
